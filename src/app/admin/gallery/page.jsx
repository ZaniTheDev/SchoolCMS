export default async function AdminGalleryPage() {
  const session = await getServerSession(authOptions);
  if (!session?.user || session.user.role !== "ADMIN") redirect("/login");

  const images = await prisma.galleryImage.findMany({
    orderBy: { createdAt: "desc" },
  });
  const sidebarNavigation = [
    {
      title: "Umum",
      items: [
        { label: "Dashboard", href: "/admin", icon: <Icons.Dashboard /> },
      ],
    },
    {
      title: "Konten",
      items: [
        { label: "Postingan", href: "/admin/posts", icon: <Icons.Posts /> },
        {
          label: "Guru & Staf",
          href: "/admin/teachers",
          icon: <Icons.Teachers />,
        },
        { label: "Acara", href: "/admin/events", icon: <Icons.Events /> },
        { label: "Galeri", href: "/admin/gallery", icon: <Icons.Gallery /> },
      ],
    },
    {
      title: "Sistem",
      items: [
        {
          label: "Pengaturan",
          href: "/admin/settings",
          icon: <Icons.Settings />,
        },
      ],
    },
  ];

  return (
    <div className="min-h-screen bg-[#f0f7ff]">
      {/* Sidebar */}

      <AdminSidebar />

      <main className="lg:pl-64">
        <div className="px-4 py-6 sm:px-6 lg:px-8 max-w-7xl mx-auto space-y-6">
          {/* Header */}
          <div className="flex items-center justify-between">
            <div>
              <h1 className="text-2xl font-bold text-[#1e3a5f]">Galeri</h1>
              <p className="text-sm text-[#64748b] mt-1">
                {images.length} foto tersimpan
              </p>
            </div>
          </div>

          {/* Upload card */}
          <div className="bg-white rounded-2xl border border-[#ddeef8] shadow-sm p-6">
            <h2 className="text-xs font-semibold text-[#64748b] uppercase tracking-wider mb-4">
              Unggah Foto Baru
            </h2>
            <GalleryUploader />
          </div>

          {/* Preview Card */}
          <div className="bg-white rounded-2xl border border-[#ddeef8] shadow-sm p-6">
            {images.length === 0 ? (
              <div className="py-16 text-center">
                <p className="text-[#94a3b8] text-sm">
                  Belum ada foto di galeri.
                </p>
              </div>
            ) : (
              <div className="grid grid-cols-2 sm:grid-cols-3 md:grid-cols-4 gap-4">
                <GalleryGrid initialImages={images} />
              </div>
            )}
          </div>
        </div>
      </main>
    </div>
  );
}
